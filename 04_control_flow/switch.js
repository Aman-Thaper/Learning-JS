let month = 2

switch (month) {
    case 1:
        console.log("It is the first month");
        break;
    case 2:
        console.log("It is the second month");
        break;
    case 3:
        console.log("It is the third month");
        break;
    

    default:
        console.log("Default case match");
        break;
}